/// production .NET license server. If the Swift side fails to verify
/// what Kotlin accepts, the embedded public key or the signing-data
/// builder has drifted from .NET — a wire-format breakage that would
/// silently reject every customer activation.
///
/// Test-sweep 2026-05-12: this whole suite is NEW. Until this test sweep
/// the Swift library had **zero** tests covering `ApiLicense` or
/// `LicenseValidator` — silent breakage was previously possible without
/// any CI signal.
